#import "YCCalendarIndexPath.h"

@implementation YCCalendarIndexPath

+ (YCCalendarIndexPath *)indextPathWithLine:(NSInteger)line andRow:(NSInteger)row
{
    YCCalendarIndexPath *indexPath = [[YCCalendarIndexPath alloc] init];
    indexPath -> _line = line;
    indexPath -> _row = row;
    return indexPath;
}

@end
